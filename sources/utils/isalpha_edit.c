#include "minishell.h"

int	ft_noalpha_edit(int c)
{
	int	a;

	a = 1;
	if (c == '*' || c == D_QUOTE || c == S_QUOTE || c == ':' || c == ';' || c == '\\')
		a = 0;
	return (a);
}

int	ft_isalpha_edit(int c)
{
	int	a;

	a = 0;
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') 
		|| c == '-' || (c >= '0' && c <= '9') || c == DOLLAR
		|| c == '/' || c == '=')
		a = 1;
	return (a);
}
