#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t dstsize)
{
	size_t			i;
	unsigned int	cont;

	i = 0;
	cont = 0;
	while (src[cont])
		cont++;
	if (dstsize == 0)
		return (cont);
	while (src[i] && i < (dstsize - 1))
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (cont);
}
